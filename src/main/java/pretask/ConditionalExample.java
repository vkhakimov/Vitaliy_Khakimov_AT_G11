package pretask;

public class ConditionalExample {
    public static void main(String[] args) {
        int x = 11;
        if (x < 10) {
            System.out.println("X is less than 10");
        } else if (x > 10) {
            System.out.println("X is greater than 10");
        } else {
            System.out.println("X is equal 10");
        }
    }
}
