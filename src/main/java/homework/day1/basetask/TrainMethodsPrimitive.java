package homework.day1.basetask;

public class TrainMethodsPrimitive {
    public void printInt(int number) {
        System.out.println("я получил число " + number);
    }

    public void printLong(long number) {
        System.out.println("я получил длинное число " + number);
    }

    public void printChar(char number) {
        System.out.println("я получил на вход символ " + number);
    }

    public void printFloat(float number) {
        System.out.println("я получил дробное число " + number);
    }

    public void printDouble(double number) {
        System.out.println("я получил длинное дробное число " + number);
    }

    public void printShort(short number) {
        System.out.println("я получил короткое число " + number);
    }

    public void printByte(byte number) {
        System.out.println("я получил очень короткое число " + number);
    }

    public void printBoolean(boolean bool) {
        System.out.println("я получил на вход булево " + bool);
    }
}
